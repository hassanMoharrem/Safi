<?php

namespace App\Http\Controllers\User;

use App\Http\Controllers\Base\BaseController;
use App\Models\DessertStation;
use Illuminate\Http\Request;

class DessertStationController extends BaseController
{
    public function index()
    {
        $data = DessertStation::all()->where('user_id',auth()->id());
        return response()->json([
            'status' => 200,
            'message' => 'Success',
            'success' => true,
            'data' => $data,
        ]);
    }
    public function show($id)
    {
        $station = DessertStation::query()->where('id',$id)->where('user_id', auth()->id())->first();
        if (!$station) {
            return response()->json([
                'status' => 403,
                'message' => 'You are not authorized to view this update phase.',
                'success' => false
            ], 403);
        }
        $model = DessertStation::class;
        return parent::showBase($id, $model); // TODO: Change the autogenerated stub
    }

    public function store(Request $request)
    {
        $model = DessertStation::class;
        $params = $request->all();
        $params['user_id'] = auth()->id();
        $rules = [
            'name'=>'required|string|min:3|max:100',
            'phase'=>'required|numeric|min:1',
        ];
        return parent::storeBase($model, $params, $rules); // TODO: Change the autogenerated stub
    }
    public function update($id)
    {
        $station = DessertStation::query()->where('id',$id)->where('user_id', auth()->id())->first();
        if (!$station) {
            return response()->json([
                'status' => 403,
                'message' => 'You are not authorized to view this update phase.',
                'success' => false
            ], 403);
        }
        $model = DessertStation::class;
        $params = \request()->all();
        $params['user_id'] = auth()->id();
        $rules = [
            'name'=>'required|string|min:3|max:100',
            'phase'=>'required|numeric|min:1',
        ];
        return parent::updateBase($id, $model, $params, $rules); // TODO: Change the autogenerated stub
    }
    public function destroy($id)
    {
        $station = DessertStation::query()->where('id',$id)->where('user_id', auth()->id())->first();
        if (!$station) {
            return response()->json([
                'status' => 403,
                'message' => 'You are not authorized to view this update phase.',
                'success' => false
            ], 403);
        }
        $model = DessertStation::class;
        return parent::destroyBase($id, $model); // TODO: Change the autogenerated stub
    }
}
